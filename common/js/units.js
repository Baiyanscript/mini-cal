// units.js - 单位换算数据表
// 每个分类下的单位: name(名称), shortname(简称), muti(相对标准单位的倍数), plus(温度类偏移量)

function makeUnit(name, shortname, muti, plus) {
  return {
    name: name,
    shortname: shortname,
    muti: muti,
    plus: plus || 0,
    to: function (v) {
      return v * this.muti + this.plus
    },
    back: function (v) {
      return (v - this.plus) / this.muti
    }
  }
}

function makeType(name, negative, unitDefs) {
  var list = []
  for (var i = 0; i < unitDefs.length; i++) {
    var u = unitDefs[i]
    list.push(makeUnit(u[0], u[1], u[2], u[3]))
  }
  return { name: name, list: list, negative: !!negative }
}

